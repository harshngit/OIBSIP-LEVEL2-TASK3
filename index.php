                <h1 class="name">Mahendra Singh Dhoni</h1>
                <p>Mahendra Singh Dhoni (commonly known as M. S. Dhoni) is an Indian cricketer who captained 
                    the Indian team in limited-overs formats from 2007 to 2016 and in Test cricket from 2008 to 2014. 
                    An attacking right-handed middle-order batsman and wicket-keeper, he is widely regarded as one of the
                     greatest finishers in limited-overs cricket.He made his One Day International (ODI) debut in December 
                     2004 against Bangladesh, and played his first Test a year later against Sri Lanka.</p>
                <p>Renowned for his cool temperament under pressure, Dhoni led India to several significant victories, including the 2007 ICC World Twenty20, 
                    the 2010 and 2016 Asia Cups, the 2011 ICC Cricket World Cup, and the 2013 ICC Champions Trophy. He was also a highly effective 
                    wicketkeeper-batsman, known for his lightning-quick stumpings and match-winning performances with the bat. Dhoni's impact on Indian
                     cricket transcends his on-field achievements, making him one of the most iconic figures in the sport's history.</p>
